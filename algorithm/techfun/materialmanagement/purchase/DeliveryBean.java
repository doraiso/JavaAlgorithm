package algorithm.techfun.materialmanagement.purchase;

public class DeliveryBean {
	private String koubai;
	private String buhin;
	private String shiire;
	private String shireName;
	private int quantity;

	public String getKoubai() {
		return koubai;
	}
	public void setKoubai(String koubai) {
		this.koubai = koubai;
	}
	public String getBuhin() {
		return buhin;
	}
	public void setBuhin(String buhin) {
		this.buhin = buhin;
	}
	public String getShiire() {
		return shiire;
	}
	public void setShiire(String shire) {
		this.shiire = shire;
	}
	public String getShireName() {
		return shireName;
	}
	public void setShireName(String shireName) {
		this.shireName = shireName;
	}
	public int getQuantity() {
		return quantity;
	}
	public void setQuantity(int quantity) {
		this.quantity = quantity;
	}

}
